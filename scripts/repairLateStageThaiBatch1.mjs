for (const [lessonId, rows] of Object.entries(fixes)) {
  const file = path.join(SOURCE_DIR, `${lessonId}-source.json`);
  const data = JSON.parse(fs.readFileSync(file, "utf8"));

  if (data.rows.length !== rows.length) {
    throw new Error(
      `${lessonId}: expected ${data.rows.length} rows, got ${rows.length} fixes`,
    );
  }

  rows.forEach((fix, rowIndex) => {
    const row = data.rows[rowIndex];
    row.thai = fix.thai;

    if (row.breakdown.length !== fix.breakdownThai.length) {
      throw new Error(
        `${lessonId}: row ${rowIndex + 1} expected ${row.breakdown.length} breakdown chunks, got ${fix.breakdownThai.length}`,
      );
    }

    fix.breakdownThai.forEach((thai, chunkIndex) => {
      row.breakdown[chunkIndex].thai = thai;
    });
  });

  fs.writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  console.log(`repaired ${lessonId}`);
}
